<template>
<!-- 高级表单常见于一次性输入和提交大批量数据的场景 -->
  <page-header-wrapper content="">
    <a-card class="card" title="仓库管理" :bordered="false">
      <repository-form ref="repository" :showSubmit="false" />
    </a-card>
    <a-card class="card" title="任务管理" :bordered="false">
      <task-form ref="task" :showSubmit="false" />
    </a-card>

</template>

<script>
import RepositoryForm from './RepositoryFrom'
import TaskForm from './TaskForm'
import FooterToolBar from '@/components/FooterToolbar'

const fieldLabels = {
  name: '仓库名',
  url: '仓库域名',
  owner: '仓库管理员',
  approver: '审批人',
  dateRange: '生效日期',
  type: '仓库类型',
  name2: '任务名',
  url2: '任务描述',
  owner2: '执行人',
  approver2: '责任人',
  dateRange2: '生效日期',
  type2: '任务类型'
}
export default {
  name: "AdvancedForm",
  components: {
    RepositoryForm,
    TaskForm,
    FooterToolBar
  },
  setup() {
    const columns = [
      {
          title: '成员姓名',
          dataIndex: 'name',
          key: 'name',
          width: '20%',
          scopedSlots: { customRender: 'name' }
        },
        {
          title: '工号',
          dataIndex: 'workId',
          key: 'workId',
          width: '20%',
          scopedSlots: { customRender: 'workId' }
        },
        {
          title: '所属部门',
          dataIndex: 'department',
          key: 'department',
          width: '40%',
          scopedSlots: { customRender: 'department' }
        },
        {
          title: '操作',
          key: 'action',
          scopedSlots: { customRender: 'operation' }
        }
    ]
    const data = [
       {
          key: '1',
          name: '小明',
          workId: '001',
          editable: false,
          department: '行政部'
        },
        {
          key: '2',
          name: '李莉',
          workId: '002',
          editable: false,
          department: 'IT部'
        },
        {
          key: '3',
          name: '王小帅',
          workId: '003',
          editable: false,
          department: '财务部'
        }
    ]
    const handleSubmit = (e) => {
      e.preventDefault()
    }
    const newMember = () => {
      const length = this.data.length
      this.data.push({
        key: length === 0 ? '1' : (parseInt(this.data[length - 1].key) + 1).toString(),
        name: '',
        workId: '',
        department: '',
        editable: true,
        isNew: true
      })
    }
    const remove = (key) => {
      const newData = data.filter(item => item.key !== key)
      data = newData
    }
    const saveRow = (record) => {
      this.memberLoading = true
      const { key, name, workId, department } = record
      if (!name || !workId || !department) {
        this.memberLoading = false
        this.$message.error('请填写完整成员信息。')
        return
      }
      // 模拟网络请求、卡顿 800ms
      new Promise((resolve) => {
        setTimeout(() => {
          resolve({ loop: false })
        }, 800)
      }).then(() => {
        const target = this.data.find(item => item.key === key)
        target.editable = false
        target.isNew = false
        this.memberLoading = false
      })
    }
    return {
      fieldLabels,
      loading: false,
      memberLoading: false,
      columns,
      data,
      errors: [],
      handleSubmit,
      newMember,
      remove,
      saveRow,
      toggle,
      getRowByKey,
      cancel,
      handleChange,
      validate,
      errorList,
      scrollToField
    };
  },
};
</script>

